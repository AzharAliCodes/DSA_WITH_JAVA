//print square pattern using for loop
class Q1_PrintSquarePattern{
    public static void main(String[] args) {
        System.out.println("printing square pattern");
        for (int i = 0; i < 4; i++) {
            for (int j = 0; j < 4; j++) {
                System.out.print("* ");//printing star
    }
    System.out.println("");//next line
}
}
}